<h1>I Follow My Dreams</h1>
<body>
<pre>I get laughed at,
I get ignored,
I often feel trapped,
and I keep my thoughts stored.
People can be cruel and very mean,
but no matter what,
I follow my dreams.

Life has waves;
I know that.
But I stand brave
and just take the crap.
I may feel exhausted and totally creamed,
but no matter what,
I follow my dreams.

I know what I want,
and I won't stop trying.
Quitting? I can't,
for now I'm flying.
It's impossible it seems,
but no matter what,
I follow my dreams...
</pre>
